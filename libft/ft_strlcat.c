#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	char		*d;
	const char	*s;
	size_t		n;
	size_t		ds;

	d = dst;
	s = src;
	n = size;
	while (n-- && *d)
		d++;
	ds = d - dst;
	n = size - ds;
	if (n == 0)
		return (ds + ft_strlen(s));
	while (*s)
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (ds + (s - src));
}
