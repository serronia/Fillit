int		ft_atoi(const char *str)
{
	int		i;
	int		s;
	long	r;
	int		rr;

	i = 0;
	s = 1;
	r = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10;
		r = r + (str[i] - 48);
		i++;
	}
	rr = (int)r;
	return (rr * s);
}
