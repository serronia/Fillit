#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*t;

	i = 0;
	t = (char*)s;
	while (t[i] != '\0')
	{
		if (t[i] == (char)c)
		{
			return (t + i);
		}
		i++;
	}
	if ((char)c == '\0')
		return (t + i);
	else
		return (NULL);
}
